"use strict";
// Mutiplicação
function mutiplicacaoNumeros(xNumero, yNumero) {
    return xNumero * yNumero;
}
const resultadoMultiplicacao = mutiplicacaoNumeros(28, 56);
console.log(resultadoMultiplicacao);
// Saudação
function saudacao(nome) {
    return "ola " + nome;
}
const saudacaoMenssagem = saudacao("Ricardo");
console.log(saudacaoMenssagem);
